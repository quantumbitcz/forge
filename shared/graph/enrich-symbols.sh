FILE_COUNT=0

for file in "${FILES[@]}"; do
  # Resolve to relative path within project
  local_path="$file"
  # If absolute, make relative
  if [[ "$local_path" == /* ]]; then
    local_path="${local_path#${PROJECT_ROOT}/}"
  fi

  abs_path="${PROJECT_ROOT}/${local_path}"

  if [[ ! -f "$abs_path" ]]; then
    echo "// Skipping ${local_path} — file not found" >&2
    continue
  fi

  lang="$(ext_to_lang "$local_path")"
  if [[ "$lang" == "unknown" ]]; then
    echo "// Skipping ${local_path} — unsupported language" >&2
    continue
  fi

  echo "// --- ${local_path} (${lang}) ---"

  case "$lang" in
    kotlin)               extract_kotlin "$local_path" "$abs_path" ;;
    java)                 extract_java "$local_path" "$abs_path" ;;
    typescript|javascript) extract_typescript "$local_path" "$abs_path" ;;
    python)               extract_python "$local_path" "$abs_path" ;;
    go)                   extract_go "$local_path" "$abs_path" ;;
    rust)                 extract_rust "$local_path" "$abs_path" ;;
    ruby)                 extract_ruby "$local_path" "$abs_path" ;;
    php)                  extract_php "$local_path" "$abs_path" ;;
    csharp)               extract_csharp "$local_path" "$abs_path" ;;
    swift)                extract_swift "$local_path" "$abs_path" ;;
    elixir)               extract_elixir "$local_path" "$abs_path" ;;
    scala)                extract_scala "$local_path" "$abs_path" ;;
    c|cpp)                extract_c_cpp "$local_path" "$abs_path" ;;
    dart)                 extract_dart "$local_path" "$abs_path" ;;
  esac

  echo ""

  # Record enrichment
  echo "$local_path" >> "$ENRICHED_FILE"
  ((FILE_COUNT++)) || true
done

echo "// --- Enrichment Complete ---"
echo "// Files enriched: ${FILE_COUNT}"
