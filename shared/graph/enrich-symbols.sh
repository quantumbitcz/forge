      fi
    fi
  done

  # Functions
  grep -nE '^\s*(override\s+)?(static\s+)?func\s+\w+' "$abs_path" 2>/dev/null | while IFS= read -r line; do
    local fname
    fname="$(echo "$line" | sed -E 's/^[0-9]+:\s*(override\s+)?(static\s+)?func\s+(\w+).*/\3/')"
